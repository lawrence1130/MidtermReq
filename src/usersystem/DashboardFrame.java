    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int row = tblUsers.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Select a user from the table first!");
        return;
    }

    String id = tblUsers.getValueAt(row, 0).toString();
    String newName = txtEditName.getText(); // Assuming your text field is txtEditName

    try {
        java.sql.Connection conn = DBConnection.getConnection();
        String sql = "UPDATE users SET fullname=? WHERE id=?";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, newName);
        pst.setString(2, id);
        
        pst.executeUpdate();
        javax.swing.JOptionPane.showMessageDialog(this, "Update Successful!");
        loadData(); // Refresh the table
    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tblUsers.getSelectedRow();
    
        // 1. Check if a row is selected to avoid NullPointerException
        if (row == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a user from the table to delete.");
            return;
        }

        // 2. Get the ID from the first column (index 0)
        String id = tblUsers.getValueAt(row, 0).toString();

        // 3. Ask for confirmation
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
                "Are you sure you want to delete this record?", "Confirm Deletion", 
                javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try {
                java.sql.Connection conn = DBConnection.getConnection();
                String sql = "DELETE FROM users WHERE id = ?";
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, id);

                pst.executeUpdate();
                javax.swing.JOptionPane.showMessageDialog(this, "User deleted successfully.");

                // 4. Refresh the table so the deleted user disappears
                loadData(); 

                // 5. Optional: Clear the Edit Name field
                txtEditName.setText("");

            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

