    public void loadData() {
        // 1. Define the column headers to match your JTable design
        String[] columns = {"ID", "Full Name", "Username"};
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(null, columns);

        try {
            java.sql.Connection conn = DBConnection.getConnection();
            // 2. Select only the columns you want to display
            String sql = "SELECT id, fullname, username FROM users";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery();

            while(rs.next()) {
                // 3. Extract data from the Result Set
                String id = rs.getString("id");
                String name = rs.getString("fullname");
                String user = rs.getString("username");

                // 4. Add the data as a new row in the model
                String[] rowData = {id, name, user};
                model.addRow(rowData);
            }

            // 5. Set the model to your JTable
            tblUsers.setModel(model);

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
        }
    }
