    private void tblUsersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsersMouseClicked
        int row = tblUsers.getSelectedRow();

        // Safety check to ensure the row and cell are not empty
        if (row != -1 && tblUsers.getValueAt(row, 1) != null) {
            String name = tblUsers.getValueAt(row, 1).toString();
            txtEditName.setText(name); // Now the name appears in the box for editing
        }
    }//GEN-LAST:event_tblUsersMouseClicked

