    private void txtConfirmPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtConfirmPasswordActionPerformed
        String fullname = txtFullName.getText();
        String username = txtUsername.getText();
        String password = new String(txtPassword.getPassword());
        String confirmPass = new String(txtConfirmPassword.getPassword());

        // 1. Check if any fields are empty
        if(fullname.isEmpty() || username.isEmpty() || password.isEmpty() || confirmPass.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "All fields are required!");
            return;
        }

        // 2. CHECK IF PASSWORDS MATCH
        if (!password.equals(confirmPass)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Passwords do not match!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return; // Stop the registration process
        }

        // 3. Proceed with Database Insertion (Existing Logic)
        try {
            java.sql.Connection conn = DBConnection.getConnection();
            String sql = "INSERT INTO users (fullname, username, password) VALUES (?, ?, ?)";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, fullname);
            pst.setString(2, username);
            pst.setString(3, password);

            pst.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this, "Registration Successful!");

            // Clear all fields
            txtFullName.setText("");
            txtUsername.setText("");
            txtPassword.setText("");
            txtConfirmPassword.setText("");

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_txtConfirmPasswordActionPerformed

    private void chkShowPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkShowPasswordActionPerformed
        if (chkShowPassword.isSelected()) {
            // (char)0 reveals the text in both fields
            txtPassword.setEchoChar((char)0);
            txtConfirmPassword.setEchoChar((char)0);
        } else {
            // '•' hides the text in both fields
            txtPassword.setEchoChar('•');
            txtConfirmPassword.setEchoChar('•');
        }
    }//GEN-LAST:event_chkShowPasswordActionPerformed

