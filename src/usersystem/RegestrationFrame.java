    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String fullname = txtFullName.getText();
        String username = txtUsername.getText();
        String password = String.valueOf(txtPassword.getPassword());

        // Check if fields are empty
        if(fullname.isEmpty() || username.isEmpty() || password.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "All fields are required!");
            return;
        }

        try {
            java.sql.Connection conn = DBConnection.getConnection();
            // The table name MUST match what you created in phpMyAdmin
            String sql = "INSERT INTO users (fullname, username, password) VALUES (?, ?, ?)";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, fullname);
            pst.setString(2, username);
            pst.setString(3, password);

            pst.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this, "Registration Successful!");

            // Clear fields for next entry
            txtFullName.setText("");
            txtUsername.setText("");
            txtPassword.setText("");

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Registration Failed: " + e.getMessage());
        }
        
        new LoginFrame().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnRegisterActionPerformed

